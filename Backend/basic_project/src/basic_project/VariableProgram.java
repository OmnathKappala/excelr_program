package basic_project;

public class VariableProgram {

	public static void main(String[] args) {
		
		
		// value - value is a primitive data type
		int value;
//		System.out.println(value);
		
		value=10;   // Assigning or initializing
		System.out.println(value);
		
		
		value=20; // re-assing 
		
		System.out.println(value);
		
		// value2-value2 is a non primitive data type
		String value2 = "Noodles"; // Declaring and assigning 
		System.out.println(value2);

	}

}
