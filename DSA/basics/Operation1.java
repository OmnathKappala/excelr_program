class Operation{

public static void main(String[]args){
int a=20;
int b=10;
// Arithmetic operations
System.out.println(a+b);
System.out.println(a-b);
System.out.println(a*b);
System.out.println(a/b);
System.out.println(a%b);
//comparsion Operator--boolean
System.out.println(10>20);//false
System.out.println(10<20);//true
System.out.println(10>=20);//true
System.out.println(10<=20);//true
System.out.println(10==20);//false
System.out.println(10!=20);//true
 
}
}