class Alphabets{
    public static void main(String[] args) {
        char start='A';
        char end='Z';
        do { 
            System.out.println(start);
            start++;
        } while (start<=end);

        start='a';
        end='z';
        
        while(start<=end){
            System.out.println(start);
            start++;
        }

   
    
    }
}

